// 관계 연산자 활용해보기

#include <stdio.h>

int main(void)
{
	int a = 10;
	int b = 20;

	printf("a = %d, b = %d\n", a, b);
	printf("a > b = %d\n", a > b);
	printf("a >= b = %d\n", a >= b);
	printf("a < b = %d\n", a < b);
	printf("a <= b = %d\n", a <= b);
	printf("a == b = %d\n", a == b);
	printf("a != b = %d\n", a != b);

	return 0;
}
